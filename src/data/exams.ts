export function getExamsByCategory(categoryId: string): Exam[] {
  const cat = categories.find(c => c.id === categoryId);
  if (!cat) return [];
  const subCats = categories.filter(c => c.parentId === categoryId).map(c => c.id);
  const allCatIds = [categoryId, ...subCats];
  return exams.filter(e => allCatIds.includes(e.categoryId));
}

export function getTopCategories(): Category[] {
  return categories.filter(c => c.parentId === null);
}

export function getSubCategories(parentId: string): Category[] {
  return categories.filter(c => c.parentId === parentId);
}

export function searchExams(query: string): Exam[] {
  const q = query.toLowerCase();
  return exams.filter(e =>
    e.name.toLowerCase().includes(q) ||
    e.shortName.toLowerCase().includes(q) ||
    e.conductingBody.toLowerCase().includes(q) ||
    e.categoryId.toLowerCase().includes(q)
  );
}

export function getExamBySlug(slug: string): Exam | undefined {
  return exams.find(e => e.slug === slug);
}